// Generates app/lib/data/photo-dims.generated.ts from the photos referenced
// in app/lib/data/photos.ts, using sharp to read real width/height so
// next/image can render with zero layout shift.
import { readFile, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";
import sharp from "sharp";

const ROOT = path.resolve(import.meta.dirname, "..");
const PHOTOS_TS = path.join(ROOT, "app/lib/data/photos.ts");
const PUBLIC_DIR = path.join(ROOT, "public");
const OUT_FILE = path.join(ROOT, "app/lib/data/photo-dims.generated.ts");

const source = await readFile(PHOTOS_TS, "utf8");

// Match `image: "/photos/xyz.jpg"` (ignore commented-out entries whose
// preceding `//` would still match the regex, so strip full-line comments
// first).
const uncommented = source
  .split("\n")
  .filter((line) => !line.trim().startsWith("//"))
  .join("\n");

const imagePaths = [...uncommented.matchAll(/image:\s*"(\/photos\/[^"]+)"/g)].map((m) => m[1]);

if (imagePaths.length === 0) {
  throw new Error(`No image paths found in ${PHOTOS_TS}`);
}

const missing = imagePaths.filter((p) => !existsSync(path.join(PUBLIC_DIR, p)));
if (missing.length > 0) {
  throw new Error(`Missing photo file(s) referenced by photos.ts:\n  ${missing.join("\n  ")}`);
}

const entries = await Promise.all(
  imagePaths.map(async (imagePath) => {
    const { width, height } = await sharp(path.join(PUBLIC_DIR, imagePath)).metadata();
    if (!width || !height) {
      throw new Error(`Could not read dimensions for ${imagePath}`);
    }
    return [imagePath, { w: width, h: height }];
  })
);

const dims = Object.fromEntries(entries);

const body = Object.entries(dims)
  .map(([key, { w, h }]) => `  "${key}": { w: ${w}, h: ${h} },`)
  .join("\n");

const output = `// AUTO-GENERATED by scripts/gen-photo-dims.mjs — do not edit by hand.
// Run \`npm run gen:dims\` to regenerate after adding/removing photos.

export const photoDims: Record<string, { w: number; h: number }> = {
${body}
};
`;

await writeFile(OUT_FILE, output, "utf8");
console.log(`Wrote ${entries.length} photo dimensions to ${path.relative(ROOT, OUT_FILE)}`);
